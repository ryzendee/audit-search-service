package ryzendee.app.api;

/**
 * Интерфейс, описывающий поле, по которому может выполняться поиск.
 *
 * <p>Реализации должны предоставлять имя поля в источнике данных и информацию о том,
 * поддерживает ли это поле точное совпадение (exact match).</p>
 *
 * <p>Этот интерфейс используется для построения поисковых запросов и агрегаций,
 * абстрагируясь от конкретного источника или структуры данных.</p>
 *
 * @author Dmitry Ryazantsev
 */
public interface SearchableField {

    /**
     * Возвращает имя поля в источнике данных.
     *
     * @return имя поля
     */
    String getFieldName();

    /**
     * Указывает, поддерживает ли поле точное совпадение (exact match) при поиске.
     *
     * @return {@code true}, если поддерживается точное совпадение, иначе {@code false}
     */
    boolean isSupportExactMatch();
}
